from typing import Tuple

import alphalens
import pandas as pd


class FactorEvaluator:
    """
    Evaluates individual cross-sectional alpha factors using the Alphalens library.
    
    This class handles the required transformations to convert aligned, wide-format 
    factor data into the specific long-format MultiIndex structure expected by 
    Alphalens, and generates standardized tear sheets for performance analysis.
    """
    
    def format_for_alphalens(self, aligned_factor: pd.DataFrame, prices: pd.DataFrame) -> pd.DataFrame:
        """
        Formats aligned factor data and pricing data into an Alphalens compatible structure.
        
        Args:
            aligned_factor (pd.DataFrame): Wide-format factor data (index: dates, columns: tickers).
            prices (pd.DataFrame): Historical pricing data (e.g., 'close' or 'adjusted_close') 
                                   structured identically to the aligned_factor.
                                   
        Returns:
            pd.DataFrame: The "clean factor data" generated by Alphalens, ready for evaluation.
                          Contains aligned factor quantiles and forward returns metrics.
        """
        # 1. Stack the wide-format factor DataFrame into a long Series.
        #    This automatically drops NaN values where a ticker wasn't trading.
        factor_long = aligned_factor.stack()
        
        # 2. Assign the specific MultiIndex names Alphalens expects.
        factor_long.index.names = ['date', 'asset']
        factor_long.name = 'factor' # Ensure the Series has a clear name
        
        # 3. Generate clean factor and forward returns using Alphalens utility
        clean_factor_data = alphalens.utils.get_clean_factor_and_forward_returns(
            factor=factor_long,
            prices=prices,
            quantiles=5,          # Default: Quintile groupings
            periods=(1, 5, 10),   # Default: 1-day, 5-day, and 10-day forward returns
            filter_zscore=None    # Disable automatic outlier filtering by default
        )
        
        return clean_factor_data

    def generate_summary_tear_sheet(self, clean_factor_data: pd.DataFrame) -> None:
        """
        Generates and displays the standard Alphalens summary tear sheet.
        
        This output includes crucial predictive performance metrics such as
        Information Coefficient (IC) summary, cumulative quantile returns, and turnover.
        
        Args:
            clean_factor_data (pd.DataFrame): The output from `format_for_alphalens()`.
        """
        if clean_factor_data is None or clean_factor_data.empty:
            raise ValueError("The provided clean_factor_data is empty or invalid.")
            
        return alphalens.tears.create_summary_tear_sheet(clean_factor_data)
